export const fetchWarehouseProject = async (id: string): Promise<WarehouseProject | null> => {
  const { data, error } = await supabase
    .from('warehouse_projects')
    .select('*')
    .eq('id', id)
    .single();
  if (error) throw error;
  return (data || null) as WarehouseProject | null;
};

export const createWarehouseProjectFromInbox = async (
  inboxItem: WarehouseProjectInboxItem
): Promise<WarehouseProject> => {
  const { data: { user } } = await supabase.auth.getUser();

  // Insert warehouse project — project_number is auto-generated by trigger
  const { data: wp, error: wpError } = await supabase
    .from('warehouse_projects')
    .insert({
      name: inboxItem.client_name || 'Lagerprojekt',
      source_project_id: inboxItem.source_type === 'project' ? inboxItem.source_id : null,
      source_large_project_id: inboxItem.source_type === 'large_project' ? inboxItem.source_id : null,
      source_project_number: inboxItem.source_project_number,
      status: 'planning',
      created_by: user?.id ?? null,
      project_number: '', // trigger fills this
    } as any)
    .select()
    .single();

  if (wpError) throw wpError;

  // Mark inbox as converted
  await supabase
    .from('warehouse_project_inbox')
    .update({
      status: 'converted',
      processed_at: new Date().toISOString(),
      warehouse_project_id: wp.id,
    })
    .eq('id', inboxItem.id);

  return wp as WarehouseProject;
};

export const updateWarehouseProject = async (
  id: string,
  updates: Partial<WarehouseProject>
): Promise<void> => {
  const { error } = await supabase
    .from('warehouse_projects')
    .update(updates)
    .eq('id', id);
  if (error) throw error;
};

export const deleteWarehouseProject = async (id: string): Promise<void> => {
  const { error } = await supabase
    .from('warehouse_projects')
    .delete()
    .eq('id', id);
  if (error) throw error;
};

// ============================================================================
// Tasks (free moments inside a warehouse project)
// ============================================================================
export const fetchWarehouseProjectTasks = async (
  warehouseProjectId: string
): Promise<WarehouseProjectTask[]> => {
  const { data, error } = await supabase
    .from('warehouse_project_tasks')
    .select('*')
    .eq('warehouse_project_id', warehouseProjectId)
    .order('sort_order', { ascending: true });
  if (error) throw error;
  return (data || []) as WarehouseProjectTask[];
};

