import { supabase } from "@/integrations/supabase/client";
import {
  WarehouseProject,
  WarehouseProjectInboxItem,
  WarehouseProjectTask,
} from "@/types/warehouseProject";

// ============================================================================
// Inbox
// ============================================================================
export const fetchInbox = async (
  status: 'new' | 'all' = 'new'
): Promise<WarehouseProjectInboxItem[]> => {
  let q = supabase
    .from('warehouse_project_inbox')
    .select('*')
    .order('created_at', { ascending: false });
  if (status === 'new') q = q.eq('status', 'new');
  const { data, error } = await q;
  if (error) throw error;
  return (data || []) as WarehouseProjectInboxItem[];
};

export const dismissInboxItem = async (id: string): Promise<void> => {
  const { error } = await supabase
    .from('warehouse_project_inbox')
    .update({ status: 'dismissed', processed_at: new Date().toISOString() })
    .eq('id', id);
  if (error) throw error;
};

// ============================================================================
// Warehouse Projects
